                <img class="responsive-img satura" src="img/2_ecocapitalismo.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Ecocapitalismo</h1>
                <div class="contenido">
                    <p>Es la visión de que el capital existe en la naturaleza como "capital natural" (ecosistemas que tienen un rendimiento ecológico) del que depende toda la riqueza y, por lo tanto, los instrumentos de política gubernamental capitalista basados
                        en el mercado (como un impuesto al carbono) deben ser utilizados para resolver problemas ambientales.</p>
                </div>
            </div>
            <div class="col s12 m6 der">
                <img class="responsive-img sepia" src="img/3_ecosocialismo.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Ecosocialismo</h1>
                <div class="contenido">
                    <p>
                        Es un doctrina política que integra las ideas del socialismo y las del ecologismo. Los ecosocialistas piensan que el capitalismo es un sistema inherentemente dañino tanto para la sociedad como para el medio ambiente; denominados también rojiverdes a personas
                        que profesan la ideología ecosocialista, pues defienden unas políticas verdes desde una óptica claramente postcapitalista.
                    </p>
                </div>
            </div>
        </div>
        <div class="row peques">
            <div class="col s12 m6 izq">
                <img class="responsive-img sepia" src="img/4_desarrollo_sustentable.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Desarrollo sustentable</h1>
                <div class="contenido">
                    <p>
                        Es la mejora continua que permite satisfacer las necesidades actuales sin comprometer la capacidad de las futuras generaciones para satisfacer las propias. Es un proceso para mantener un balance dinámico entre la demanda de equidad, prosperidad y una
                        mejor calidad de vida lo que es posible ecológicamente.
                    </p>
                </div>
            </div>
            <div class="col s12 m6 der">
                <img class="responsive-img satura" src="img/5_responsabilidad_social_empresarial.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">RSE</h1>
                <div class="contenido">
                    <p>Se refiere esencialmente, a través del cual las empresas deciden voluntariamente contribuir al logro de una sociedad mejor y un medio ambiente más limpio. Esta responsabilidad se expresa frente a los trabajadores y, en general, frente
                        a todos los interlocutores de la organización, que pueden a su vez influir en su éxito.</p>
                </div>
            </div>
        </div>
        <div class="row completo">
            <div class="col s12 col-completo">
                <img class="responsive-img" src="img/7_eco_eficiencia.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Ecoeficiencia</h1>
                <div class="contenido">
                    <p>Específicamente se refiere a: producir más con menos deterioro ambiental. Es el objetivo último de la empresa que adopta algún tipo de medida para mejorar su desempeño ambiental.</p>
                </div>
            </div>
        </div>
        <div class="row peques">
            <div class="col s12 m6 izq">
                <img class="responsive-img satura" src="img/6_enfoque_globa_empresas_rse.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">RSE Enfoque Global</h1>
                <div class="contenido">
                    <p>Indica que las empresas tienen que integrar la responsabilidad social en la gestión cotidiana de toda su cadena de producción, sus trabajadores y directivos necesitan medidas de formación y reciclaje para adquirir las cualificaciones
                        y competencias necesarias. Las empresas pioneras pueden ayudar a generalizar las prácticas socialmente responsables difundiendo las mejores prácticas.</p>
                </div>
            </div>
            <div class="col s12 m6 der">
                <img class="responsive-img sepia" src="img/8_enercia_renobable.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Energías renovables</h1>
                <div class="contenido">
                    <p>
                        Se le denomina así a la energía que se obtiene de fuentes naturales virtualmente inagotables, ya sea por la inmensa cantidad de energía que contienen, o porque son capaces de regenerarse por medios naturales; tales como la energía eólica, la geotérmica,
                        la hidroeléctrica, la mareomotriz, la solar, la undimotriz, la biomasa y los biocarburantes.
                    </p>
                </div>
            </div>
        </div>
        <div class="row peques">
            <div class="col s12 m6 izq">
                <img class="responsive-img sepia" src="img/9_bioeconomia.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Bioeconomía</h1>
                <div class="contenido">
                    <p>
                        Actividades económicas que obtienen productos y servicios, generando valor económico, al utilizar como elementos fundamentales los recursos biológicos; donde su objetivo es la producción y comercialización de alimentos, así como productos forestales,
                        bioproductos y bioenergía, obtenidos mediante transformaciones físicas, químicas, bioquímicas o biológicas de la materia orgánica no destinada al consumo.
                    </p>
                </div>
            </div>
            <div class="col s12 m6 der">
                <img class="responsive-img satura" src="img/10_basura_cero.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Basura Cero</h1>
                <div class="contenido">
                    <p>
                        Se refiere a un concepto de vida sustentable, basado en la reutilización de residuos que de otra manera serían almacenados y/o incinerados, contribuyendo así a la descontaminación del ambiente. Busca reducir la producción de residuos, reciclar y revalorizar
                        la mayor cantidad posible de materiales, así como promover la fabricación de producto de larga vida útil.
                    </p>
                </div>
            </div>
        </div>
        <div class="row completo">
            <div class="col s12 col-completo">
                <img class="responsive-img" src="img/12_economia_circular.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Economía circular</h1>
                <div class="contenido">
                    <p>
                        Se basa en un innovador diseño como paradigma de manufactura llamado “Cradle to Cradle” o “de la Cuna-a-la-Cuna”. Este nuevo paradigma implica una perspectiva de diseño que busca la eco-eficacia en primer lugar y donde se enfoca en el desarrollo de procesos
                        industriales o de manufactura donde los materiales utilizados para la creación de un producto se convierten en valiosos nutrientes al llegar a satisfacer su uso por el consumidor final y haber alcanzado el fin de vida útil.
                    </p>
                </div>
            </div>
        </div>
        <div class="row peques">
            <div class="col s12 m6 izq">
                <img class="responsive-img satura" src="img/11_quimica_verde.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Química Verde o Sostenible</h1>
                <div class="contenido">
                    <p>
                        Este término es sinónimo de salud y de sostenibilidad ambiental. Fundamentalmente está orientada a buscar nuevas formas de sintetizar sustancias químicas para lograr una química más amigable con la salud y el entorno. Su razón de ser, es encontrar alternativas
                        a las propuestas por la química tradicional, que en tantas ocasiones representa un peligro para la salud y también para el medio ambiente.
                    </p>
                </div>
            </div>
            <div class="col s12 m6 der">
                <img class="responsive-img sepia" src="img/13_economia_recursos_naturales_.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Economía de los Recursos Naturales</h1>
                <div class="contenido">
                    <p>
                        Se encarga de analizar la oferta, la demanda y la asignación de los recursos naturales de la Tierra. Uno de los objetivos principales es comprender mejor el papel de los recursos naturales en la economía con el fin de desarrollar métodos más sostenibles
                        en la gestión de los recursos para así asegurar su disponibilidad para las generaciones futuras.
                    </p>
                </div>
            </div>
        </div>
        <div class="row peques">
            <div class="col s12 m6 izq">
                <img class="responsive-img sepia" src="img/14_huella_ecologica.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Huella Ecológica</h1>
                <div class="contenido">
                    <p>
                        Es un indicador biofísico de sostenibilidad integrador que se define como; el total de superficie ecológicamente productiva necesaria para producir los recursos consumidos por un ciudadano medio de una determinada comunidad humana, así como la necesaria
                        para absorber las emisiones que genera, independientemente de la localización de estas superficies.
                    </p>
                </div>
            </div>
            <div class="col s12 m6 der">
                <img class="responsive-img satura" src="img/15_deuda_ecologica.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Deuda Ecológica</h1>
                <div class="contenido">
                    <p>
                        Se basa en la idea de justicia ambiental: así, si todos los habitantes del planeta tienen derecho a la misma cantidad de recursos y a la misma porción de espacio ambiental, los que usan más recursos utilizan y ocupan más espacio tienen una deuda hacia
                        los otros.
                    </p>
                </div>

            </div>
        </div>
