            <div class="col s12 col-completo">
                <img class="responsive-img" src="img/17_solidaridad.jpg" alt="Capitalismo">
                <h1 class="tituloP" id="tituloP">Economía Solidaria</h1>
                <div class="contenido">
                    <p>
                        Se refiere a una forma de producción, consumo, y distribución de riqueza, específicamente a un tipo de economía; que se centra en la valorización del ser humano y no en la priorización del capital; al promoverla asociatividad, la cooperación y la autogestión,
                        orientada a la producción, al consumo, y a la comercialización de bienes y servicios, de un modo principalmente autogestionado, teniendo como finalidad el desarrollo ampliado de la vida.
                    </p>
